function switchOn() {
  var light = document.getElementById("light");
  light.style.backgroundImage = "url('./assets/images/switch-on.png')"; // switch on
}

function switchOff() {
  var light = document.getElementById("light");
  light.style.backgroundImage = "url('./assets/images/switch-off.png')"; // switch off
}

